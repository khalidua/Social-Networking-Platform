param(
    [string]$RepoRoot = (Resolve-Path (Join-Path $PSScriptRoot "..\..")).Path,
    [double]$MinimumCoverage = 80,
    [switch]$SkipReportWrite
)

$ErrorActionPreference = "Stop"

$targets = @(
    @{
        Service = "auth-service"
        Package = "./internal/service"
        Scope = "OAuth login, callback, JWT session validation, logout"
    },
    @{
        Service = "users-service"
        Package = "./internal/service"
        Scope = "profile lifecycle, follow/unfollow rules, follower lookup"
    },
    @{
        Service = "posts-service"
        Package = "./internal/service"
        Scope = "post CRUD rules, ownership checks, interaction events"
    },
    @{
        Service = "feed-service"
        Package = "./internal/service"
        Scope = "feed read and Redis failure fallback behavior"
    },
    @{
        Service = "notification-service"
        Package = "./internal/service"
        Scope = "notification read/create validation and persistence calls"
    }
)

$env:GOCACHE = Join-Path $RepoRoot ".gocache"
$env:GOTELEMETRY = "off"
$profileDir = Join-Path $env:GOCACHE "coverage"
New-Item -ItemType Directory -Force -Path $profileDir | Out-Null

$results = @()

foreach ($target in $targets) {
    $serviceDir = Join-Path $RepoRoot $target.Service
    if (-not (Test-Path $serviceDir)) {
        throw "Service directory not found: $serviceDir"
    }

    Push-Location $serviceDir
    $profile = Join-Path $profileDir "$($target.Service)-core.out"
    try {
        Write-Host "Running core coverage for $($target.Service) $($target.Package)"
        & go test $target.Package -count=1 "-coverprofile=$profile"
        if ($LASTEXITCODE -ne 0) {
            throw "go test failed for $($target.Service)"
        }

        $coverageOutput = & go tool cover -func $profile
        if ($LASTEXITCODE -ne 0) {
            $coverageOutput | ForEach-Object { Write-Host $_ }
            throw "go tool cover failed for $($target.Service)"
        }

        $totalLine = $coverageOutput | Where-Object { $_ -match "^total:" } | Select-Object -Last 1
        if (-not $totalLine) {
            throw "Could not find total coverage line for $($target.Service)"
        }
        if ($totalLine -notmatch "([0-9]+(\.[0-9]+)?)%") {
            throw "Could not parse coverage percentage from: $totalLine"
        }

        $coverage = [double]$Matches[1]
        $results += [pscustomobject]@{
            Service = $target.Service
            Package = $target.Package
            Scope = $target.Scope
            Coverage = $coverage
            Passed = $coverage -ge $MinimumCoverage
        }
    }
    finally {
        Pop-Location
    }
}

$failed = $results | Where-Object { -not $_.Passed }

$lines = @(
    "# Core Unit Coverage Report",
    "",
    "Generated by ``tests\coverage\run-core-coverage.ps1``.",
    "",
    "Minimum required core business logic coverage: $MinimumCoverage%",
    "",
    "| Service | Package | Core scope | Coverage | Status |",
    "| --- | --- | --- | ---: | --- |"
)

foreach ($result in $results) {
    $status = if ($result.Passed) { "Pass" } else { "Fail" }
    $lines += "| {0} | ``{1}`` | {2} | {3}% | {4} |" -f $result.Service, $result.Package, $result.Scope, $result.Coverage.ToString("0.0"), $status
}

if (-not $SkipReportWrite) {
    $reportPath = Join-Path $PSScriptRoot "core-coverage-report.md"
    $lines | Set-Content -LiteralPath $reportPath -Encoding UTF8
    Write-Host "Wrote coverage report to $reportPath"
}

$lines | ForEach-Object { Write-Host $_ }

if ($failed) {
    $names = ($failed | ForEach-Object { "$($_.Service)=$($_.Coverage.ToString("0.0"))%" }) -join ", "
    throw "Core coverage below $MinimumCoverage%: $names"
}
